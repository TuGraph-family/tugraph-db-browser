import request from '../utils/request';
import {
  NodeQueryParams,
  PathQueryParams,
  StatementParams,
} from '../interface/query';
/* 语句查询接口 */
export async function statementQuery(params: StatementParams) {
  return request(`/api/query/language`, {
    method: 'POST',
    data: {
      ...params,
    },
  });
}

